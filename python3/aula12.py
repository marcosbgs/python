nome = 'Marcos Vinicius'
altura = 1.70
peso = 88.5
imc = peso / (altura * altura)

# alternativa: imc = peso / altura ** 2

print(f'{nome} tem {altura:.2f} de altura, pesa {peso} quilos e seu IMC é {imc:.2f}.')

#f-strings
# para derterminar a quantidade de casas decimais usa-se :.2f(duas casa decimais) ou :.3f(três casa decimais) 

